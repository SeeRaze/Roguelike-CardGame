import random  # Импортируем модуль для перемешивания карт

class DeckManager:
    """
    Чертеж менеджера колоды. 
    Он полностью управляет круговоротом карт: Добор -> Рука -> Сброс.
    """
    def __init__(self, starting_pool):
        # Сохраняем изначальный стартовый набор карт в постоянный карман
        self.pool = starting_pool
        
        # Создаем три пустые коробки (списка) для разных состояний карт
        self.draw_pile = []     # Колода добора (откуда тянем)
        self.hand = []          # Рука игрока (что держим и можем разыграть)
        self.discard_pile = []  # Сброс (куда уходят сыгранные карты)

        # Сразу при рождении менеджера запускаем процесс подготовки колоды
        self.reset_deck()

    def reset_deck(self):
        """Команда полной перезагрузки колоды (например, при старте нового боя)"""
        # .copy() делает независимый дубликат списка карт пула, чтобы не сломать оригинал pool
        self.draw_pile = self.pool.copy()
        
        # Очищаем руку и сброс, чтобы в начале боя они были абсолютно пустыми
        self.hand.clear()
        self.discard_pile.clear()
        
        # Намертво перемешиваем нашу колоду добора
        random.shuffle(self.draw_pile)
        print("--- Менеджер Колоды: Все карты собраны в добор и перемешаны! ---")

    def draw_cards(self, amount):
        """Безопасный пацанский добор карт с защитой от бесконечных циклов"""
        print(f"--- Менеджер Колоды: Пытаемся добрать карт: {amount} ---")
        
        for _ in range(amount):
            # Если колода добора опустела, пытаемся вернуть карты из сброса
            if not self.draw_pile:
                if self.discard_pile:
                    print("--- Сброс перемешивается обратно в колоду добора! ---")
                    self.draw_pile = self.discard_pile.copy()
                    self.discard_pile.clear()
                    random.shuffle(self.draw_pile)
                else:
                    # ЗАЩИТА: Если и сброс, и добор пусты (все карты в руке) — 
                    # просто тихо прекращаем добор, не вешая игровой цикл
                    print("[!] Больше нет карт для добора (все карты в руке).")
                    break
            
            # Безопасно извлекаем карту, только если в доборе что-то появилось
            if self.draw_pile:
                card = self.draw_pile.pop()
                self.hand.append(card)
                print(f" Взяли в руку карту: {card.name}")

    def discard_hand(self):
        """Команда сброса ВСЕХ карт из руки в концу хода"""
        # Оптом высыпаем все элементы из руки в сброс за один микрошаг
        self.discard_pile.extend(self.hand) 
        self.hand.clear()
        print("--- Менеджер Колоды: Все карты из руки отправлены в сброс! ---")
