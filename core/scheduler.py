# core/scheduler.py
# ОЧЕРЕДЬ ОТЛОЖЕННЫХ ЭФФЕКТОВ — кросс-каттинг сабсистем «сработать через N ходов» (§3).
#
# Зачем: под будущие механики-таймеры, без bespoke-кода у каждой. Потребители:
# Heat-перегрев (Кузнец: недоковал → урон через ход), тайм-бомбы / Эффект Бабочки
# (Парадокс), любые «через N ходов сделать X». Сейчас потребителя нет → очередь пуста
# и ИНЕРТНА (baseline зелёный), как субстрат позиционки до §1.
#
# Чистый примитив: очередь хранит ТОЛЬКО тайминг (turns + действие), ничего не исполняет
# сама. `tick()` уменьшает счётчики и ВОЗВРАЩАЕТ созревшие — исполнение оставлено
# вызывателю (CombatManager выполняет их под _guarded_action → конечность каскада).
# Разделение пьюр-тайминга и исполнения: (1) тривиально тестируется без боя; (2) действие,
# запланировавшее НОВОЕ отложенное во время исполнения, не теряется (tick уже завершился).


class DelayedEffect:
    """Запланированное действие: сработать через `turns` ходов. `action(combat_manager)`
    вызывается вызывателем при созревании. `label` — метка для лога."""

    __slots__ = ("turns", "action", "label")

    def __init__(self, turns, action, label=""):
        self.turns = max(1, int(turns))   # пол 1: «через 0 ходов» = «в конце этого хода»
        self.action = action
        self.label = label


class DelayedQueue:
    """Очередь отложенных эффектов (обычно — на CombatManager, скоуп боя). Пьюр-тайминг:
    `schedule` кладёт действие, `tick` уменьшает таймеры и отдаёт созревшие вызывателю.
    Инертна при пустой очереди (`tick` → [])."""

    def __init__(self):
        self._items = []

    def schedule(self, turns, action, label=""):
        """Запланировать `action(cm)` через `turns` ходов (пол 1)."""
        self._items.append(DelayedEffect(turns, action, label))

    def __len__(self):
        return len(self._items)

    @property
    def pending(self) -> list:
        """Копия списка запланированных (для UI/инспекции, без мутации очереди)."""
        return list(self._items)

    def tick(self) -> list:
        """Уменьшить ВСЕ таймеры на 1; вернуть список СОЗРЕВШИХ (turns<=0) и убрать их
        из очереди. Исполнение — на вызывателе (под его гардом каскада). Действие,
        планирующее новое отложенное при исполнении, безопасно: tick уже переустановил
        очередь до возврата, новые элементы лягут в неё, а не потеряются."""
        if not self._items:
            return []
        due, survivors = [], []
        for item in self._items:
            item.turns -= 1
            (due if item.turns <= 0 else survivors).append(item)
        self._items = survivors
        return due
