# core/debt.py
# ДОЛГОВОЙ ДВИЖОК — овердрафт ресурсов («power now, pay later»). Спека §7
# _rulestack_design.md. Срезы: ДОЛГ ЭНЕРГИИ (С46) + ДОЛГ HP (С49, субстрат Берсерка).
#
# Суть примитива: ресурс может уйти В МИНУС при розыгрыше (овердрафт) → глубина долга
# даёт МНОЖИТЕЛЬ урона (заём силы) → расплата (энергия: HP-процент в конце хода;
# HP: близость к смерти + хук класса). Это «вторая экспонента», риск-driven (не
# card-driven): сила сейчас, платёж потом.
#
# Чистый модуль: ТОЛЬКО конфиг-ручки + pure-формула множителя. Без pygame, без боевой
# логики, без побочек на импорте (как core/forge.py). Механика (гейт/пол/гашение) живёт
# в CombatManager/Creature; этот файл — источник правды чисел и кривых ПО РЕСУРСАМ.
#
# Обобщение (С49): общая pure-формула `_debt_multiplier(depth, mode, per_point, exp_rate)`,
# по обёртке на ресурс (energy_/hp_debt_multiplier). Каждая читает СВОИ ручки живьём →
# рубильник "exp" работает per-resource, энергетический срез байт-в-байт со старым кодом.
#
# ── ГАРД-РЕЙЛЫ (амплитуда, не эффект) ─────────────────────────────────────────
#   *_MAX_OVERDRAFT — жёсткий ПОЛ долга ресурса: глубже не уйти (аналог TriggerGuard, но
#   для экономики). Безумие ограничено экономикой, раннер не взрывается числами.

# Линейная отдача долга: +DEBT_DAMAGE_PER_POINT к множителю за 1 ед. долга энергии.
# Дефолт безопасен для симулятора (предсказуемо, без взрыва).
DEBT_DAMAGE_PER_POINT = 0.25

# РУБИЛЬНИК КРИВОЙ (ручка-модификатор): "linear" (дефолт) | "exp".
#   linear: mult = 1 + debt * DEBT_DAMAGE_PER_POINT
#   exp:    mult = (1 + DEBT_EXP_RATE) ** debt   ← «вторая экспонента» (потолок, §7)
# Экспонента заложена, но ВЫКЛ по умолчанию — включается одной ручкой позже.
DEBT_CURVE_MODE = "linear"
DEBT_EXP_RATE = 0.20

# Жёсткий пол долга: максимальный |энергия| в минусе (амплитудный гард-рейл).
DEBT_MAX_OVERDRAFT = 4

# Гашение: HP-штраф за каждую ед. непогашенного долга в конце хода (процент-«заём»).
DEBT_HP_INTEREST = 2

# ── ДОЛГ HP (С49 субстрат Берсерка; С57 — ПРОЦЕНТНАЯ, МАСШТАБ-ИНВАРИАНТНАЯ ОСЬ) ──
# HP уходит НИЖЕ 0 (овердрафт жизни) → глубина минуса даёт множитель урона. «Расплата»
# структурная: чем глубже минус, тем ближе пол-смерть. Грацию-ход и конверсию |HP|→FP при
# победе подключает КЛАСС Берсерка (хук, не ядро). Инертно без флага hp_overdraft.
#
# С57 (юзер): пол и множитель — от ДОЛИ max HP, не флат. Прежний фикс-пол (−10→−30) ломался
# с РОСТОМ max HP (события/артефакты/экспонента): нырок в −30 при 1000 HP = капля пула,
# движок отмирал ([[balance-curve-framework]] — ось игрока обязана расти с экспонентой).
# Теперь «нырок на X% пула» даёт ТУ ЖЕ награду при любом max HP. На базовых 60 HP числа
# ИДЕНТИЧНЫ прежним (пол −30, дно ×4.0) → обобщение без регресса.
#
# Пол = «точка невозврата» (линия смерти), НЕ потолок награды. Регулятор глубины — СТРОГАЯ
# РАСПЛАТА (конец хода в минусе при живых врагах → смерть): глубже = больше урон И больше
# уязвимость. Класс НЕ тронут — только эти две ручки. exp-«спайс» (выпуклая кривая) — на
# потом, если линейного мало.
HP_DEBT_FLOOR_RATIO = 0.5        # пол долга = −50% от max HP (на 60 → −30; на 1000 → −500)
HP_DEBT_MULT_PER_RATIO = 6.0     # +6.0 к множителю за ПОЛНЫЙ max HP долга (на 60 HP ≡ 0.10/HP)


def hp_debt_floor(max_hp: int) -> int:
    """Пол HP-долга = −HP_DEBT_FLOOR_RATIO × max_hp (точка невозврата). ПРОЦЕНТ, не флат:
    растёт с max HP → механика не отмирает при росте экспоненты (1000 HP → пол −500).
    max_hp<=0 → 0 (инертно, как обычный класс)."""
    return -int(HP_DEBT_FLOOR_RATIO * max(0, max_hp))


def hp_debt_multiplier(depth: int, max_hp: int) -> float:
    """Множитель урона игрока от ДОЛГА HP (depth = |hp| при hp<0), МАСШТАБ-ИНВАРИАНТНЫЙ:
    зависит от ДОЛИ max HP в долге (depth/max_hp), не от абсолюта → нырок на ту же долю
    пула даёт ту же награду при любом max HP. На 60 HP ≡ прежней 1+0.10·глубина (дно −30 →
    ×4.0). depth<=0 или max_hp<=0 → 1.0 (инертно). Pure (считается и в превью урона)."""
    if depth <= 0 or max_hp <= 0:
        return 1.0
    return 1.0 + HP_DEBT_MULT_PER_RATIO * (depth / max_hp)


def _debt_multiplier(depth: int, curve_mode: str, per_point: float,
                     exp_rate: float) -> float:
    """ОБЩАЯ pure-кривая множителя урона от глубины долга (depth>0). Ресурс-агностична —
    обёртки ниже подставляют свои ручки. depth<=0 → 1.0 (инертно). Без побочек."""
    if depth <= 0:
        return 1.0
    if curve_mode == "exp":
        return (1.0 + exp_rate) ** depth
    return 1.0 + depth * per_point


def energy_debt_multiplier(debt: int) -> float:
    """Множитель урона игрока от глубины ДОЛГА энергии (debt = |energy| при energy<0).

    debt<=0 → 1.0 (инертно: нормальная игра без овердрафта). Кривая выбирается
    DEBT_CURVE_MODE: линейная по умолчанию (безопасно), экспонента — рубильником.
    Читает ручки живьём → байт-в-байт со старым кодом. Pure (считается и в превью)."""
    return _debt_multiplier(debt, DEBT_CURVE_MODE, DEBT_DAMAGE_PER_POINT, DEBT_EXP_RATE)
