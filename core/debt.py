# core/debt.py
# ДОЛГОВОЙ ДВИЖОК — овердрафт ресурсов («power now, pay later»). Спека §7
# _rulestack_design.md. Вертикальный срез: ДОЛГ ЭНЕРГИИ (обобщение минус-HP Берсерка).
#
# Суть примитива: ресурс может уйти В МИНУС при розыгрыше (овердрафт) → глубина долга
# даёт МНОЖИТЕЛЬ урона (заём силы) → в конце хода долг ГАСИТСЯ HP-процентом (расплата).
# Это «вторая экспонента», риск-driven (не card-driven): сила сейчас, платёж потом.
#
# Чистый модуль: ТОЛЬКО конфиг-ручки + pure-формула множителя. Без pygame, без боевой
# логики, без побочек на импорте (как core/forge.py). Механика (гейт/гашение) живёт в
# CombatManager; этот файл — источник правды чисел и кривой.
#
# ── ГАРД-РЕЙЛЫ (амплитуда, не эффект) ─────────────────────────────────────────
#   DEBT_MAX_OVERDRAFT — жёсткий ПОЛ долга: глубже не уйти (аналог TriggerGuard, но
#   для экономики). Безумие ограничено экономикой, раннер не взрывается числами.

# Линейная отдача долга: +DEBT_DAMAGE_PER_POINT к множителю за 1 ед. долга энергии.
# Дефолт безопасен для симулятора (предсказуемо, без взрыва).
DEBT_DAMAGE_PER_POINT = 0.25

# РУБИЛЬНИК КРИВОЙ (ручка-модификатор): "linear" (дефолт) | "exp".
#   linear: mult = 1 + debt * DEBT_DAMAGE_PER_POINT
#   exp:    mult = (1 + DEBT_EXP_RATE) ** debt   ← «вторая экспонента» (потолок, §7)
# Экспонента заложена, но ВЫКЛ по умолчанию — включается одной ручкой позже.
DEBT_CURVE_MODE = "linear"
DEBT_EXP_RATE = 0.20

# Жёсткий пол долга: максимальный |энергия| в минусе (амплитудный гард-рейл).
DEBT_MAX_OVERDRAFT = 4

# Гашение: HP-штраф за каждую ед. непогашенного долга в конце хода (процент-«заём»).
DEBT_HP_INTEREST = 2


def energy_debt_multiplier(debt: int) -> float:
    """Множитель урона игрока от глубины ДОЛГА энергии (debt = |energy| при energy<0).

    debt<=0 → 1.0 (инертно: нормальная игра без овердрафта). Кривая выбирается
    DEBT_CURVE_MODE: линейная по умолчанию (безопасно), экспонента — рубильником.
    Pure-функция (без побочек) → считается и в превью урона (dry_run)."""
    if debt <= 0:
        return 1.0
    if DEBT_CURVE_MODE == "exp":
        return (1.0 + DEBT_EXP_RATE) ** debt
    return 1.0 + debt * DEBT_DAMAGE_PER_POINT
