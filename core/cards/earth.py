# core/cards/earth.py
# Карты стихии «Земля» (статус Раскол). Архетип — КОНТРА БРОНЕ: пока на цели есть
# щит, Раскол множит входящий урон ×3 (см. EffectCalculator, шаг 4b). Связка —
# навесить Раскол на щитового врага/босса и пробить его щит за пару ударов.
#
# Трио:
#   «Камнепад»          — дешёвый энейблер, чистое наложение Раскола.
#   «Дробящий удар»     — дешёвый эксплойт: бьёт мало, но сквозь Раскол ×3 по щиту.
#   «Тектонический удар» — гибрид: урон + наложение Раскола разом.
from core.cards.base import Card, DamageEffect, StatusEffect
from core.rarity import Rarity


def create_rockfall():
    """«Камнепад» — накладывает Раскол 2(3) хода. Чистый энейблер за 1 энергии:
    готовит щитового врага под пробивание."""
    return Card(
        name="Камнепад",
        cost=1,
        card_type="skill",
        description="Накладывает Раскол 2(3) х. Пока у цели есть щит — урон по ней ×3.",
        effects=[
            StatusEffect("shatter", 2, 3),
        ],
    )


def create_crush():
    """«Дробящий удар» — урон 4(6). Дёшев: payoff бьёт через Раскол ×3, пока на
    цели держится щит (4 → 12 по щитовому врагу)."""
    return Card(
        name="Дробящий удар",
        cost=1,
        card_type="attack",
        description="Урон 4(6). Против цели с Расколом и щитом бьёт ×3.",
        effects=[
            DamageEffect(4, 6),
        ],
    )


def create_tectonic_strike():
    """«Тектонический удар» — урон 6(8) + Раскол 2(3). Гибрид: бьёт сейчас и вешает
    Раскол под последующие удары по щиту."""
    return Card(
        name="Тектонический удар",
        cost=2,
        card_type="attack",
        description="Урон 6(8). Накладывает Раскол 2(3) х.",
        effects=[
            DamageEffect(6, 8),
            StatusEffect("shatter", 2, 3),
        ],
        rarity=Rarity.UNCOMMON,
    )
