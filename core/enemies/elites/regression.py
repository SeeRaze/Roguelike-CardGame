# core/enemies/elites/regression.py
# Регрессия — элита-контра «медленным» билдам (валидатор гласс-кэннона).
# Механика «ЗАКРЫВАЮЩЕЕСЯ ОКНО»: каждый выживший ход элита ТВЕРДЕЕТ — копит
# нарастающую броню (щит +RAMP/ход) и слегка регенерирует. DPS-окно убийства
# закрывается: бёрст-залп в первые ходы успевает пробить, затяжной гринд — уже нет.
# Прямой тест ПИКА урона (Берсерк DPS-bound). Тема: «то, что работало вчера,
# сегодня — уже нет» (регрессионный баг — некогда рабочее ломается само).
# Обход: ранний глубокий нырок-бёрст; урон сквозь щит (яд/кровь/Казнь); снести до
# того, как накопленная броня перекроет залп.
import random
from core.enemies.elites.base import EliteBase


class RegressionElite(EliteBase):
    """Элита-контра отложенному/затяжному урону (валидатор бёрста).

    Начало хода игрока (on_turn_start): накопитель `hardening` растёт на
    HARDEN_RAMP, выставляется как щит (gain_shield) + малый реген REGEN_PER_TURN.
    Щит обнуляется в начале вражеской фазы (phases.py) → накопленная броня
    защищает ИМЕННО в фазу игрока, когда он бьёт. Каждый следующий ход игрок
    встречает всё более толстый барьер — окно убийства закрывается.
    Боевая логика — простая атака/защита (основная угроза = растущая броня).

    Мягкие обходы:
    - Бёрст/высокий пик урона: пробивает броню в ранние ходы (окно ещё открыто)
    - Урон сквозь щит (яд/кровь/Казнь): игнорирует накопленную броню
    - Не «глухая стена»: реген мал, броня сбрасывается на атаке врага — это
      налог на МЕДЛИТЕЛЬНОСТЬ, а не неубиваемость
    """

    HARDEN_RAMP     = 4   # прирост накопленной брони за каждый выживший ход игрока
    REGEN_PER_TURN  = 3   # малый реген/ход (затяжной чип-урон не накапливается)

    _TITLES = [
        "Регрессия",
        "Накопленный Техдолг",
        "Энтропия Системы",
    ]

    def __init__(self, name, hp, max_hp):
        super().__init__(name=name, hp=hp, max_hp=max_hp)
        # Накопитель брони. Растёт на HARDEN_RAMP в каждый on_turn_start.
        self.hardening = 0

    @staticmethod
    def random_title() -> str:
        return random.choice(RegressionElite._TITLES)

    # ── Хук реакции ──────────────────────────────────────────────────────

    def on_turn_start(self, player, combat_manager) -> None:
        """Твердеет: копит нарастающую броню + слегка регенерирует.
        Чем дольше длится бой — тем толще барьер, который надо пробить за ход."""
        self.hardening += self.HARDEN_RAMP
        self.gain_shield(self.hardening, combat_manager)
        if self.hp < self.max_hp:
            self.heal(self.REGEN_PER_TURN, combat_manager)
        if combat_manager:
            combat_manager.add_log_message(
                f"[РЕГРЕССИЯ] Затвердевает: +{self.hardening} щита "
                f"(окно убийства закрывается)."
            )

    # ── Боевая логика ───────────────────────────────────────────────────

    def choose_intent(self):
        # Преимущественно атакует — основная угроза в нарастающей броне, не в уроне.
        if self.turn_count % 3 == 2:
            self.set_intent("defend", self.base_test_shield)
        else:
            self.set_intent("attack", self.base_test_damage)
