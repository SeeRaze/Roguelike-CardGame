# core/enemies/elites/spell_eater.py
# Автоскейлинг — элита-контра колодам-пулемётам.
# Механика: +щит за КАЖДУЮ разыгранную игроком карту. Чем выше нагрузка (больше
# дешёвых карт за ход), тем больше «инстансов» поднимает автоскейлер → колоды,
# спамящие много карт, упираются в эластичную ёмкость. Обход: тяжёлые единичные
# удары (не наращивают нагрузку), урон сквозь щит (legacy+tox/Казнь), Декомпиляция
# (анти-щит). Класс-идентификатор SpellEater сохранён (инфра/реестр).
import random
from core.enemies.elites.base import EliteBase


class SpellEater(EliteBase):
    """Элита-контра колодам-пулемётам («Автоскейлинг»).

    Реакция на розыгрыш (on_card_played): +SHIELD_PER_CARD щита за каждую карту —
    система масштабируется под нагрузку. Боевая логика: преимущественно атакует
    (ёмкость растёт сама от карт игрока).

    Мягкие обходы:
    - Берсерк/бёрст: один тяжёлый удар пробивает накопленную ёмкость
    - Legacy+Токс (Кислотный дождь)/Казнь: урон сквозь щит игнорирует броню
    - Декомпиляция (decomp): анти-щит, не даёт масштабироваться
    """

    SHIELD_PER_CARD = 4   # +щита («инстанс») за каждую сыгранную игроком карту

    _TITLES = [
        "Автоскейлинг",
        "Балансировщик нагрузки",
        "Эластик-кластер",
    ]

    @staticmethod
    def random_title() -> str:
        return random.choice(SpellEater._TITLES)

    # ── Хук реакции ──────────────────────────────────────────────────────

    def on_card_played(self, card, player, combat_manager) -> None:
        """Каждая сыгранная игроком карта → +щит. Спам карт = рост нагрузки,
        автоскейлер поднимает ёмкость."""
        self.gain_shield(self.SHIELD_PER_CARD, combat_manager)
        if combat_manager:
            combat_manager.add_log_message(
                f"[АВТОСКЕЙЛИНГ] +{self.SHIELD_PER_CARD} ёмкости "
                f"под нагрузку (разыграна карта)."
            )

    # ── Боевая логика ───────────────────────────────────────────────────

    def choose_intent(self):
        # Каждый 3-й ход — лёгкая защита, остальные — атака (основная угроза —
        # накопленная от карт игрока ёмкость, поэтому intent простой).
        if self.turn_count % 3 == 2:
            self.set_intent("defend", self.base_test_shield)
        else:
            self.set_intent("attack", self.base_test_damage)
