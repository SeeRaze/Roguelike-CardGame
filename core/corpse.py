# core/corpse.py
# ТРУПЫ НА СЕТКЕ — субстрат будущего класса Некроманта (фаза «полировки ядра»).
#
# Зачем: когда существо погибает, оно НЕ исчезает бесследно — становится пассивным
# ТРУПОМ: помечается тегом [Corpse], СОХРАНЯЕТ свою 2D-координату (rank/line) и
# продолжает ЗАНИМАТЬ клетку на сетке (блокирует будущие перемещения/свапы), пока его
# кто-то не ПОГЛОТИТ или не ВЗОРВЁТ (механики Некроманта/детонаций — придут с классом).
#
# Реализация — ТЕГ на павшем существе (а не отдельный объект): мёртвый враг и так
# остаётся в self.enemies (hp<=0; victory=all(hp<=0), таргетинг фильтрует живых), поэтому
# труп = тот же объект + метка [Corpse], без дублирования состояния (урок бага cm.enemy).
#
# Чистый модуль: тег-константа + pure-ридеры/предикаты. Без pygame, без боевой логики.
# ИНЕРТЕН без потребителя (как 2D-субстрат позиционки до §1 / очередь отлож. до Heat):
# трупы помечаются, но никто их пока не читает/не двигает → baseline зелёный.

# Тег павшего существа. Строка-метка (расширяемо: Некромант/детонации проверяют теги).
CORPSE_TAG = "Corpse"


def mark_corpse(creature) -> None:
    """Пометить павшее существо трупом — добавить тег [Corpse]. 2D-координата (rank/line)
    уже на существе и НЕ трогается: труп остаётся в своей клетке. Идемпотентно
    (повторная пометка ничего не меняет). Создаёт набор тегов лениво."""
    tags = getattr(creature, "tags", None)
    if tags is None:
        tags = set()
        creature.tags = tags
    tags.add(CORPSE_TAG)


def is_corpse(creature) -> bool:
    """Является ли существо трупом (несёт тег [Corpse])."""
    return CORPSE_TAG in getattr(creature, "tags", ())


def corpses_in(creatures) -> list:
    """Трупы среди существ (для Некроманта / UI / инспекции)."""
    return [c for c in creatures if is_corpse(c)]


def cell_has_corpse(creatures, rank, line) -> bool:
    """Занята ли клетка (rank, line) трупом — предикат под БУДУЩИЕ свапы/перемещения
    (труп блокирует клетку, пока его не поглотят/взорвут). Инертен без потребителя свапов:
    сейчас никто не двигает существ по клеткам, предикат просто готов к использованию."""
    return any(
        is_corpse(c)
        and getattr(c, "rank", None) == rank
        and getattr(c, "line", None) == line
        for c in creatures
    )
