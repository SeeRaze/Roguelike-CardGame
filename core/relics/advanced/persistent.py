# core/relics/advanced/persistent.py
# Растущие реликвии — ПЕРСИСТЕНТНЫЙ СЛОЙ ПО ЗАБЕГУ (шаг №5 framework).
# Корень всех «стен» по [[balance-curve-framework]]: игрок растёт аддитивно, враг
# экспоненциально, и почти НИЧЕГО не переносится между боями. Эти реликвии копят
# КОМПАУНД через весь забег (множитель ×k за каждого побеждённого босса) — то самое
# «масштабирование категории 4», которого структурно не хватало. Состояние живёт на
# инстансе реликвии (свежий инстанс на забег → авто-сброс между забегами), хук —
# on_boss_defeated (босс-этажи 20/40/60/80/100).
from core.relics.base import Relic
from core.rarity import Rarity


class КоронаВознесения(Relic):
    """Каждый побеждённый босс: перманентный ×GROWTH_PER_BOSS к урону атак игрока
    (компаунд по забегу). 5 боссов забега → ×1.25^5 ≈ ×3.05 к финалу.

    Флагман персистентного слоя: единственный источник кат.4-компаунда, который
    переносится МЕЖДУ боями (внутрибоевые движки barrier/mastery/echo/virulence
    сбрасываются `reset_combat_statuses`). Бустит наклон кривой игрока, а не точку
    перехвата → путь к пробитию потолка (модель R(f) из framework). Замер-свип
    показал: механизм флипает наклон (Маг пробивает эт.100 при ×2.0/босс); ×1.25 —
    «заметный, но не ломающий» баланс для ОДНОЙ реликвии (слой растёт будущими)."""

    # Прирост множителя за каждого босса. Тюнинг-ручка (свип в замере баланса).
    GROWTH_PER_BOSS = 1.25

    def __init__(self):
        super().__init__(
            "Корона Вознесения",
            "Каждый побеждённый босс навсегда усиливает урон ваших атак на 25%.\n"
            "Эффект множится и сохраняется до конца забега.",
            Rarity.EPIC,
        )
        # Накопленный множитель урона. 1.0 = нет боссов; ×GROWTH за каждого.
        self._mult = 1.0

    def on_boss_defeated(self, player, combat_manager=None):
        self._mult *= self.GROWTH_PER_BOSS
        if combat_manager:
            pct = int(round((self._mult - 1.0) * 100))
            combat_manager.add_log_message(
                f"[Реликвия] '{self.name}': урон атак +{pct}% до конца забега!"
            )

    def on_damage_calculated(self, base_dmg, is_player_attack=True):
        if is_player_attack and self._mult > 1.0:
            # Округляем (не усекаем): иначе ×1.1 на мелком уроне (5→5.5) терялся бы
            # в ноль на ранних боссах. Согласовано с int()-множителями EffectCalculator.
            return int(round(base_dmg * self._mult))
        return base_dmg
