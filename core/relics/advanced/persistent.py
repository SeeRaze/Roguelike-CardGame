# core/relics/advanced/persistent.py
# Растущие реликвии — ПЕРСИСТЕНТНЫЙ СЛОЙ ПО ЗАБЕГУ (шаг №5 framework).
# Корень всех «стен» по [[balance-curve-framework]]: игрок растёт аддитивно, враг
# экспоненциально, и почти НИЧЕГО не переносится между боями. Эти реликвии копят
# КОМПАУНД через весь забег (множитель ×k за каждого побеждённого босса) — то самое
# «масштабирование категории 4», которого структурно не хватало. Состояние живёт на
# инстансе реликвии (свежий инстанс на забег → авто-сброс между забегами), хук —
# on_boss_defeated (босс-этажи 20/40/60/80/100).
from core.relics.base import Relic
from core.rarity import Rarity


class КоронаВознесения(Relic):
    """Каждый побеждённый босс: перманентный ×GROWTH_PER_BOSS к урону атак игрока
    (компаунд по забегу). 5 боссов забега → ×1.25^5 ≈ ×3.05 к финалу.

    Флагман персистентного слоя: единственный источник кат.4-компаунда, который
    переносится МЕЖДУ боями (внутрибоевые движки barrier/mastery/echo/frenzy
    сбрасываются `reset_combat_statuses`). Бустит наклон кривой игрока, а не точку
    перехвата → путь к пробитию потолка (модель R(f) из framework). Замер-свип
    показал: механизм флипает наклон (Маг пробивает эт.100 при ×2.0/босс); ×1.25 —
    «заметный, но не ломающий» баланс для ОДНОЙ реликвии (слой растёт будущими)."""

    # Прирост множителя за каждого босса. Тюнинг-ручка (свип в замере баланса).
    GROWTH_PER_BOSS = 1.25

    def __init__(self):
        super().__init__(
            "Корона Вознесения",
            "Каждый побеждённый босс навсегда усиливает урон ваших атак на 25%.\n"
            "Эффект множится и сохраняется до конца забега.",
            Rarity.EPIC,
        )
        # Накопленный множитель урона. 1.0 = нет боссов; ×GROWTH за каждого.
        self._mult = 1.0

    def on_boss_defeated(self, player, combat_manager=None):
        self._mult *= self.GROWTH_PER_BOSS
        if combat_manager:
            pct = int(round((self._mult - 1.0) * 100))
            combat_manager.add_log_message(
                f"[Реликвия] '{self.name}': урон атак +{pct}% до конца забега!"
            )

    def on_damage_calculated(self, base_dmg, is_player_attack=True, dry_run=False):
        if is_player_attack and self._mult > 1.0:
            # Округляем (не усекаем): иначе ×1.1 на мелком уроне (5→5.5) терялся бы
            # в ноль на ранних боссах. Согласовано с int()-множителями EffectCalculator.
            return int(round(base_dmg * self._mult))
        return base_dmg


class СердцеБездны(Relic):
    """Каждый побеждённый босс: +GROWTH_PCT к МАКС. HP навсегда (компаунд
    ВЫЖИВАЕМОСТИ по забегу) + мгновенный хил на прирост.

    Оборонное зеркало «Короны Вознесения»: та растит атакующую экспоненту (наклон
    урона), а это — экспоненту выживаемости (p в модели R(f) из
    [[balance-curve-framework]]). Закрывает документированный дефицит ОБОРОННОГО
    кат.4-компаунда (тот же структурный корень, что у стены Друида: атак.движки
    потолок не двигают, p не растёт от ×урон). Компаунд хранится прямо в
    `player.max_hp` (переносится между боями тем же player; свежий забег = свежий
    игрок = авто-сброс). %-прирост масштаб-инвариантен (как REST_HEAL_PCT)."""

    # Прирост макс. HP за каждого побеждённого босса. Тюнинг-ручка.
    GROWTH_PCT = 0.15

    def __init__(self):
        super().__init__(
            "Сердце Бездны",
            "Каждый побеждённый босс навсегда увеличивает макс. здоровье на 15%\n"
            "и сразу лечит на эту величину. Компаунд до конца забега.",
            Rarity.EPIC,
        )

    def on_boss_defeated(self, player, combat_manager=None):
        gain = max(1, int(round(player.max_hp * self.GROWTH_PCT)))
        player.max_hp += gain
        player.hp     += gain   # лечим на дельту — прирост полезен немедленно
        if combat_manager:
            combat_manager.add_log_message(
                f"[Реликвия] '{self.name}': +{gain} к макс. HP "
                f"(теперь {player.max_hp}) до конца забега!"
            )


class КаменьВечнойЖизни(Relic):
    """В конце КАЖДОГО боя: +GROWTH_PCT к МАКС. HP навсегда + хил на прирост.

    Второе оборонное зеркало персистентного слоя (HP-ось, economy-axis-trinity):
    Сердце Бездны копит большими спайками на боссах (×1.15 ≈ 5 раз/забег), это —
    ровным дрипом на КАЖДОЙ победе (триггер чаще босса). Малый %, но боёв за забег
    ~70-80 → 1.01^N ≈ ×2 к финалу: сопоставимо с Сердцем Бездны, иная каденция
    (две разные «удочки» оси). on_combat_end зовётся на КАЖДЫЙ бой (не только босс)."""

    # Прирост макс. HP за каждый выигранный бой. Малый — компаундит частотой.
    GROWTH_PCT = 0.01

    def __init__(self):
        super().__init__(
            "Камень Вечной Жизни",
            "В конце каждого боя макс. здоровье растёт на 1% (и лечит на прирост).\n"
            "Малый, но постоянный компаунд выживаемости до конца забега.",
            Rarity.EPIC,
        )

    def on_combat_end(self, player, combat_manager=None):
        gain = max(1, int(round(player.max_hp * self.GROWTH_PCT)))
        player.max_hp += gain
        player.hp     += gain
        if combat_manager:
            combat_manager.add_log_message(
                f"[Реликвия] '{self.name}': +{gain} к макс. HP "
                f"(теперь {player.max_hp})!"
            )
