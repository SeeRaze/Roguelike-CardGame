# core/ReactionOrder.py
# ЕДИНЫЙ ИСТОЧНИК ПРАВДЫ о порядке боевых РЕАКЦИЙ/ТРИГГЕРОВ (ревизия ядра, пункт 2).
#
# Зачем: в бою несколько реакционных систем (множительные комбо ПАР, детонации,
# Эхо-ретриггер, тики статусов, хуки реликвий/врагов). Чтобы они срабатывали
# ПРЕДСКАЗУЕМО (а не «как ляжет dict/список») и чтобы любая взаимно-рекурсивная
# цепочка ГАРАНТИРОВАННО обрывалась, порядок задаётся ДАННЫМИ здесь, в одном месте.
#
# Это чистый модуль: ТОЛЬКО данные + сортировочный хелпер. Без pygame, без боевой
# логики, без побочек. Реестры (ComboRegistry/DetonationRegistry) и CombatManager
# импортируют отсюда приоритет — никаких «магических» порядков по файлам.
#
# ── Как читать приоритет ──────────────────────────────────────────────────────
# Меньше число = РАНЬШЕ в цепочке реакций ОДНОГО события (розыгрыш карты / фаза
# врага). Внутри одной фазы реакции разрешаются по возрастанию приоритета; при
# равном приоритете — по вторичному ключу (имя записи), чтобы порядок был
# полностью детерминирован и стабилен между запусками.
#
# Связь с предохранителем глубины (core/forge.TriggerGuard, MAX_TRIGGER_DEPTH):
# приоритет задаёт ПОРЯДОК, а гард задаёт ПОТОЛОК ГЛУБИНЫ каскада. Это
# ортогональные гарантии: порядок — детерминизм, гард — конечность. Обе нужны.

from enum import IntEnum


class ReactionPriority(IntEnum):
    """Приоритет разрешения реакций внутри одного боевого события.

    Порядок отражает текущий (выверенный аудитом) ход розыгрыша карты:
      урон карты считается с КОМБО (×N) и ШОКОМ (+флат) → затем карта-детонатор
      подрывает ДЕТОНАЦИИ → затем ЭХО ретриггерит карту целиком. После розыгрыша —
      хуки реликвий и врагов. Тики статусов живут в фазе врага (end_turn_phase).

    Значения НЕ обязаны быть смежными: оставлены зазоры под будущие реакции
    (вставить новую = выбрать число между соседями, не переписывая остальные).
    """

    COMBO       = 10   # Множительные комбо (ComboRegistry: ПАР wet+ignited ×3)
    SHOCK       = 20   # Расход Шока (+флат к удару, после множителей)
    FORGE_TAG   = 30   # Условные теги прокачки карты (ForgeRegistry, ×/+)
    DETONATION  = 40   # Детонации (DetonationRegistry: электро/термо/лава/…)
    ECHO        = 50   # Эхо-ретриггер всей карты
    STATUS_TICK = 60   # Тики статусов в фазе врага (горение/яд/кровь/реген)
    RELIC_HOOK  = 70   # Хуки реликвий (on_card_played и т.п.)
    ENEMY_HOOK  = 80   # Хуки врагов/боссов (on_card_played: Архивариус +щит)


def order_keyed(records: dict, priority) -> list:
    """Вернуть (key, record) реестра в ДЕТЕРМИНИРОВАННОМ порядке.

    `records` — dict вида {key: record} (как COMBOS/DETONATIONS). `priority` —
    значение ReactionPriority этого семейства (общее для всего реестра) ИЛИ
    функция record->int для разнородных приоритетов. Вторичный ключ сортировки —
    имя записи (key), поэтому результат стабилен независимо от порядка вставки.

    Сейчас и комбо, и детонации — однородные семейства (один приоритет на реестр),
    так что фактический порядок = алфавит по key. Хелпер существует, чтобы ПОРЯДОК
    задавался ЗДЕСЬ централизованно, а не неявным dict-порядком в каждом файле."""
    if callable(priority):
        return sorted(records.items(), key=lambda kv: (priority(kv[1]), kv[0]))
    return sorted(records.items(), key=lambda kv: (int(priority), kv[0]))
